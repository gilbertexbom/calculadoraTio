# Calculadora simples

def soma(v1, v2):
    return v1 + v2

def subtração(v1, v2):
    return    v1 - v2

def mult(v1, v2):
    return v1 * v2

def divisao(v1, v2):
    return v1 / v2

def elevar_potencia(v1, v2):
     return v1 ** v2

def resto_inteiro(v1, v2):
    return v1 % v2

